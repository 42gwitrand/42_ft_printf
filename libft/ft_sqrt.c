#include "libft.h"

int		ft_sqrt(int nb)
{
	int a;
	int n;

	a = 0;
	n = 1;
	if (nb == 0)
		return (0);
	if (nb > 0)
	{
		while (n < nb)
		{
			a++;
			n = a * a;
		}
	}
	if (n == nb)
	{
		return (a);
	}
	else
		return (0);
}
